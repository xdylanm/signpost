#include "arrowanimation.h"

ArrowAnimation::ArrowAnimation(Adafruit_NeoPixel& strip_l, Adafruit_NeoPixel& strip_r) 
  : Animation(strip_l, strip_r), _is_left(true)
{
  set_left_only();
}

void ArrowAnimation::set_both()
{
  _is_left = true;
  set_frame_data(6, 24, &_ani_both[0]);
}

void ArrowAnimation::set_left_only()
{
  _is_left = true;
  set_frame_data(6, 24, &_ani_left[0]);
}

void ArrowAnimation::set_right_only()
{
  _is_left = false;
  set_frame_data(6, 24, &_ani_right[0]);
}

void ArrowAnimation::toggle()
{
  if (_is_left) {
    set_right_only();
  } else {
    set_left_only();
  }
}

